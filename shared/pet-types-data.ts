export function getPetTypeById(petId: string): PetTypeData | undefined {
  return STATIC_PET_TYPES.find((pet) => pet.petId === petId);
}

export function getPetTypesByRarity(
  rarity: "common" | "uncommon" | "rare" | "epic" | "legendary",
): PetTypeData[] {
  return STATIC_PET_TYPES.filter((pet) => pet.rarity === rarity);
}

export function getAllPetTypes(): PetTypeData[] {
  return STATIC_PET_TYPES;
}

export function getCompatiblePets(petId: string): PetTypeData[] {
  const pet = getPetTypeById(petId);
  if (!pet) return [];

  return STATIC_PET_TYPES.filter((p) => pet.friendlyTo.includes(p.petId));
}

export function getIncompatiblePets(petId: string): PetTypeData[] {
  const pet = getPetTypeById(petId);
  if (!pet) return [];

  return STATIC_PET_TYPES.filter((p) => pet.hostileTo.includes(p.petId));
}

export function canPetsBreed(petId1: string, petId2: string): boolean {
  const pet1 = getPetTypeById(petId1);
  const pet2 = getPetTypeById(petId2);

  if (!pet1 || !pet2) return false;

  if (pet1.hostileTo.includes(petId2) || pet2.hostileTo.includes(petId1)) {
    return false;
  }

  return pet1.friendlyTo.includes(petId2) || pet2.friendlyTo.includes(petId1);
}
